import java.util.Scanner;


public class Odd_Even 
{

	static int a;
	public static void main(String[] args)
	{
		Scanner sc=new Scanner(System.in);
		System.out.print("Enter the NO:");
		a=sc.nextInt();
		
		if(a%2 == 0)
		{
			System.out.println("Even No");
		}
		else
		{
			System.out.println("Odd No");
		}
		
		

	}


	
		
		
		

}
