package first_coding;



public class Three_Star
{
	
	
	public static void main(String[] args) 
	{
		
		
	
		int rc=0;
		do{
			int bc=0;
			do{
			
				bc=bc+1;
				System.out.print("*");
				System.out.print(" ");
			  }while(bc<3);
		 
			  rc=rc+1;
			  System.out.println();
  	
		 }while(rc<3);
		
		
		
	}


	
	
	
}
